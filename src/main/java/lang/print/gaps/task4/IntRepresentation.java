package lang.print.gaps.task4;

public class IntRepresentation {
    public static void main(String[] args) {
        int intC = 99;
        int intH = 104;
        int intA = 97;
        int intR = 114;

        char c = (char) intC;
        char h = (char) intH;
        char a = (char) intA;
        char r = (char) intR;

        System.out.println(c);
        System.out.println(h);
        System.out.println(a);
        System.out.println(r);
    }
}
